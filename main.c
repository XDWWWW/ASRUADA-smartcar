void MotorInit()
{
  // 马达端口使用一个PWM A6 和一个IO A8
  //  PORT_cfg.h中有上述端口定义
   ftm_pwm_init(FTM0, FTM_CH2,10000,0);//初始化FTM0CH3，频率10k,占空比0。精度为1000u
   ftm_pwm_init(FTM0, FTM_CH3,10000,0);//初始化FTM0CH3，频率10k,占空比0。精度为1000u
   gpio_init(PTA7,GPO,0);
   gpio_init(PTA8,GPO,0);
}

void MotorPower(int Power)
{
    if(Power>=0)//正转的情况
    {
        if(Power>100)//限幅
        {
            Power=100;
        }
        gpio_set(PTA7,0);
        ftm_pwm_duty(FTM0,FTM_CH2,Power);
    }
    else//反转的情况
    {
        Power=100+Power;
        if(Power>100)//限幅
        {
            Power=100;
        }
        gpio_set(PTA7,1);
        ftm_pwm_duty(FTM0,FTM_CH2,Power);
    }
}
